    private void setTextFieldsWithCheckBox(int index, boolean isBChanged, javax.swing.JTextField textBField, javax.swing.JTextField textUField, javax.swing.JCheckBox checkBox) {
        String textB = TextUtil.prepareStringToDouble(textBField.getText());
        String textU = TextUtil.prepareStringToDouble(textUField.getText());
        if(TextUtil.isStringDoubleValue(textU) && TextUtil.isStringDoubleValue(textB)) {
            if(checkBox.isEnabled() && checkBox.isSelected()) {
                double diff = diffs.get(index);
                double diffNew = Double.parseDouble(textU) - Double.parseDouble(textB);
                if (Math.abs(diff-diffNew) > 1e-5) {
                    if (isBChanged) {
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                textUField.setText(
                                        String.format(Locale.ENGLISH, NUM_FORMAT, (Double.parseDouble(textB) + diff)));
                            }
                        });
                    } else {
                        double upValue = Double.parseDouble(textU);
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                if(upValue - diff > 0 ) {
                                    double value = upValue - diff;
                                    textBField.setText(String.format(Locale.ENGLISH, NUM_FORMAT, value));
                                } else {
                                    textBField.setText(String.format(Locale.ENGLISH, NUM_FORMAT, 0));
                                }
                            }
                        });
                    }
                }
            } else {
                setCheckBox(index, textB, textU, checkBox);
            }
        } else {
            checkBox.setEnabled(false);
        }
    }

    private void setCheckBox(int index, String textB, String textU, javax.swing.JCheckBox checkBox) {
        double diff = Double.parseDouble(textU) - Double.parseDouble(textB);
        if(diff > 0) {
            checkBox.setEnabled(true);
            saveCheckBoxState(index);
        } else {
            checkBox.setEnabled(false);
        }
    }
    
