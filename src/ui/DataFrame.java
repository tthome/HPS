    private void checkAndSetEnabledFields(
            final javax.swing.JTextField textField,
            final javax.swing.JTextField textBField, 
            final javax.swing.JTextField textUField,
            final javax.swing.JCheckBox checkBox){
        final String text = textField.getText().trim();
        boolean hasText = text != null && text.length() > 0;
        if(hasText) {
            setEnabledForBoundsAndCheckBox(textBField, textUField, checkBox, false);
        } else {
            setEnabledForBoundsAndCheckBox(textBField, textUField, checkBox, true);
        }
    }
    
